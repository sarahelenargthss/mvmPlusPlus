    private void jButtonExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarActionPerformed
        // TODO add your handling code here:

        int programa = Integer.parseInt(jTextFieldNumeroPrograma.getText());
        jTextAreaTrace.setText("");
        jTextAreaMonitor.setText("");
        int maxNumInstrucoes = Integer.parseInt(jTextFieldMaxNumInstrucoes.getText());
        _cpuMVM = new CpuMVM(jTextAreaMonitor, jTextAreaTrace);
        _cpuMVM.botao = 0;
        _cpuMVM._dumpPilha = jCheckBoxDumpPilha.isSelected();
        _cpuMVM._dumpRegistradores = jCheckBox1.isSelected();
        _cpuMVM._dumpMemoria = jCheckBoxDumpMemoria.isSelected();
        _cpuMVM._traceExecuta = jCheckBoxTraceDecodificaExecuta.isSelected();
        _cpuMVM._traceBuscaDecodifica = jCheckBoxTraceBusca.isSelected();
        _cpuMVM._interrupcoesHabilitadas = jCheckBoxInterrupcoesHabilitadas.isSelected();
        
        _cpuMVM._enderecoDeCarga = Integer.parseInt(jTextFieldEnderecoCarga.getText());

        _biosMVM = new BiosMVM(_cpuMVM, _memMVM, programa, maxNumInstrucoes,_cpuMVM._enderecoDeCarga);
    }//GEN-LAST:event_jButtonExecutarActionPerformed

